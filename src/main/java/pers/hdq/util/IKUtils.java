    /**
     * @param content 传入需要分词的字符串
     * @param B       选择是否智能分词，为false将精细到最小颗粒分词
     *
     * @return 分完词的 List<String>
     * @throws
     * @Title: segStr
     * @Description: TODO
     * @author HuDaoquan
     */
    public static List<String> segStr(String content, Boolean B) {
        List<String> list = new ArrayList<>();
        try {
            // 创建分词对象
            StringReader sr = new StringReader(content);
            IKSegmenter ik = new IKSegmenter(sr, B);
            Lexeme lex;
            // 分词
            while ((lex = ik.next()) != null) {
                // 去除停用词
                if (stopWordSet.contains(lex.getLexemeText())) {
                    continue;
                }
                list.add(lex.getLexemeText());
            }
        } catch (IOException e) {
            System.out.println("IK分词器分词报错:" + e);
        }
        return list;
    }
    
